import {belongsTo, Entity, hasOne, model, property} from '@loopback/repository';
import {PersonModel} from './person-model.model';
import {Prodcut} from './prodcut.model';

@model()
export class Pedido extends Entity {
  @property({
    type: 'string',
    id: true,
    generated: true,
  })
  id?: string;

  @property({
    type: 'string',
    required: true,
  })
  id_product: string;

  @property({
    type: 'number',
    required: true,
  })
  cantidad: number;

  @property({
    type: 'number',
    required: true,
  })
  price: number;

  @property({
    type: 'number',
    required: true,
  })
  state: number;

  @belongsTo(() => PersonModel)
  personId: string;

  @hasOne(() => Prodcut)
  product: Prodcut;

  constructor(data?: Partial<Pedido>) {
    super(data);
  }
}

export interface PedidoRelations {
  // describe navigational properties here
}

export type PedidoWithRelations = Pedido & PedidoRelations;
